/**
 * Get testimonials with pagination support
 */
export const getTestimonials = (page: number = 1, limit: number = 6): {
  testimonials: Testimonial[];
  hasMore: boolean;
  total: number;
} => {
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;
  const paginatedTestimonials = testimonials.slice(startIndex, endIndex);

  return {
    testimonials: paginatedTestimonials,
    hasMore: endIndex < testimonials.length,
    total: testimonials.length
  };
};

/**
 * Get testimonials by rating
 */
export const getTestimonialsByRating = (minRating: number = 1): Testimonial[] => {
  return testimonials.filter(testimonial => testimonial.rating >= minRating);
};

/**
 * Get featured testimonials (highest rated)
 */
export const getFeaturedTestimonials = (count: number = 3): Testimonial[] => {
  return testimonials
    .filter(testimonial => testimonial.rating === 5)
    .slice(0, count);
};